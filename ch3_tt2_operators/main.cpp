#include <iostream>
#include <cmath>
using namespace std;

int main ()

{
    cout << "Please, enter an integer number: ";
    int n;
    cin >> n;


    cout << " n == " << n;
    cout << "\n n+1 == " << n+1;
    cout << "\n three times n == " << 3*n;
    cout << "\n two times n == " << n+n;
    cout << "\n n^2 == " << n*n;
    cout << "\n n/2 == " << n/2;
    double r;
    r=n;
    cout << "\n square root n == " << sqrt (r);
    cout << '\n';
}
